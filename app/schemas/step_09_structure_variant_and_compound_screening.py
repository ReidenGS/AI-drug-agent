    design_method: Literal["rfdiffusion", "proteinmpnn", "esm", "other"]
    contigs_dsl: Optional[str] = None  # deterministic template only
    output_artifact_id: Optional[str] = None
    stability_score: Optional[float] = None
    notes: Optional[str] = None


