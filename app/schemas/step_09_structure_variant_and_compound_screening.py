class CompoundScreeningArtifact(BaseModel):
    """Legacy-compatible Step 9 artifact.

    The class name and compound fields are kept to avoid breaking existing
    storage/API consumers. Current Step 9 active lanes are protein design and
    variant evaluation, so compound fields should remain empty/not_applicable.
    """

    run_id: str
    step_id: str = "step_09_structure_design"
    created_at: str
    screening_status: Literal["ok", "partial", "skipped", "failed"] = "skipped"
    compound_hits: list[CompoundHit] = Field(default_factory=list)
    tool_call_records: list[ToolCallRecord] = Field(default_factory=list)
    step9_available_fields: list[Step9AvailableField] = Field(default_factory=list)
    step9_readiness_summary: Step9ReadinessSummary = Field(default_factory=Step9ReadinessSummary)
    step9_lane_statuses: list[Step9LaneStatus] = Field(default_factory=list)
    step9_hard_gate_allowed_tools: list[Step9HardGateAllowedTool] = Field(
        default_factory=list
    )
    step9_hard_gate_blocked_tools_with_reason: list[Step9HardGateBlockedTool] = Field(
        default_factory=list
    )
    step9_tool_schema_requirements: list[Step9ToolSchemaRequirement] = Field(
        default_factory=list
    )
    step9_stage1_catalog_tool_names: list[str] = Field(default_factory=list)
    step9_stage1_selected_tools: list[dict] = Field(default_factory=list)
    step9_stage1_rejected_tools_with_reason: list[dict] = Field(default_factory=list)
    step9_stage1_selection_source: str = "not_run"
    step9_stage1_prompt_cache_layout_version: str = "not_run"
    step9_stage2_schema_survivors: list[str] = Field(default_factory=list)
    step9_stage2_mapped_tools: list[dict] = Field(default_factory=list)
    step9_stage2_uninvokable_tools: list[str] = Field(default_factory=list)
    step9_stage2_uninvokable_tool_details: list[dict] = Field(default_factory=list)
    step9_stage2_argument_mapping_audit: list[dict] = Field(default_factory=list)
    step9_stage2_prompt_cache_layout_version: str = "not_run"
    step9_runtime_execution_plan: list[dict] = Field(default_factory=list)
    step9_runtime_resolved_tools: list[dict] = Field(default_factory=list)
    step9_runtime_unresolved_tools: list[dict] = Field(default_factory=list)
    step9_runtime_resolver_audit: list[dict] = Field(default_factory=list)
    step9_runtime_kwargs_contracts: list[dict] = Field(default_factory=list)
    step9_runtime_kwargs_contract_audit: list[dict] = Field(default_factory=list)
    step9_runtime_execution_mode: str = "not_run"
    step9_missing_inputs: list[str] = Field(default_factory=list)
    # ── Step9InputProjection (centralized deterministic projection layer) ──
    # Production Stage 1 / Stage 2 / runtime planning consume ONLY these
    # fields; the legacy `step9_hard_gate_*` / `step9_available_fields`
    # fields above stay for backward-compatible audit and no longer drive
    # catalog/selection/mapping/planning.
    step9_input_fields: list[Step9InputField] = Field(default_factory=list)
    step9_input_projection_summary: dict = Field(default_factory=dict)
    step9_projection_missing_inputs: list[str] = Field(default_factory=list)
    # ── Step 9 real MCP execution (Turn C) ──────────────────────────────────
    # Populated once the runtime resolver (`step_09_runtime_execution`)
    # resolves a Stage 2 kwargs contract to real values and
    # `StructureAndDesignAgent.run_step_9` actually invokes the tool via
    # `mcp_client.call_tool`. `tool_call_records` (above) carries the
    # canonical per-call record (with `tool_output_ref` into
    # `tool_outputs/step_09/`); these two fields are a compact, redundant
    # index over the same calls for convenient Step 9 auditing.
    step9_runtime_executed_tools: list[str] = Field(default_factory=list)
    step9_runtime_execution_records: list[dict] = Field(default_factory=list)
    protein_design_readiness: Step9LaneReadinessProfile = Field(
        default_factory=Step9LaneReadinessProfile
    )
    variant_evaluation_readiness: Step9LaneReadinessProfile = Field(
        default_factory=Step9LaneReadinessProfile
    )
    compound_screening_readiness: Step9LaneReadinessProfile = Field(
        default_factory=Step9LaneReadinessProfile
    )
